package com.kr.swjch.source.bible.vo;

public class bibleKorhrvVO {
	int idx;
	int cate;
	int book;
	int chapter;
	int verse;
	String content;
	String testament;
	String long_label;
	String short_label;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getCate() {
		return cate;
	}
	public void setCate(int cate) {
		this.cate = cate;
	}
	public int getBook() {
		return book;
	}
	public void setBook(int book) {
		this.book = book;
	}
	public int getChapter() {
		return chapter;
	}
	public void setChapter(int chapter) {
		this.chapter = chapter;
	}
	public int getVerse() {
		return verse;
	}
	public void setVerse(int verse) {
		this.verse = verse;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getTestament() {
		return testament;
	}
	public void setTestament(String testament) {
		this.testament = testament;
	}
	public String getLong_label() {
		return long_label;
	}
	public void setLong_label(String long_label) {
		this.long_label = long_label;
	}
	public String getShort_label() {
		return short_label;
	}
	public void setShort_label(String short_label) {
		this.short_label = short_label;
	}
}
