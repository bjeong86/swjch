package com.kr.swjch.admin.user.vo;

public class userVO {
	int idx;
	String name;
	String famNum;
	String year;
	String month;
	String day;
	int isMoon;
	String phone;
	int isDeleted;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getFamNum() {
		return famNum;
	}
	public void setFamNum(String famNum) {
		this.famNum = famNum;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public int getIsMoon() {
		return isMoon;
	}
	public void setIsMoon(int isMoon) {
		this.isMoon = isMoon;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public int getIsDeleted() {
		return isDeleted;
	}
	public void setIsDeleted(int isDeleted) {
		this.isDeleted = isDeleted;
	}

}
